package it.unibo.smol.model;

/**
 * This enum define the possible type of entities present in the game.
 */
public enum Type {
    /**
     * The player entity.
     */
    PLAYER,

    /**
     * The enemy entity.
     */
    ENEMY,

    /**
     * The healtpoint entity.
     */
    HEALTH,

    /**
     * The weapon entity.
     */
    WEAPON,

    /**
     * The wall entity.
     */
    WALL,
}
